assume cs:codesg, ds:data, es:table

data segment
    ; 年份：21个4字节字符串（1975~1995）
    db '1975','1976','1977','1978','1979','1980','1981','1982','1983'
    db '1984','1985','1986','1987','1988','1989','1990','1991','1992'
    db '1993','1994','1995'
    ; 收入：21个4字节数值（单位：千美元）
    dd 16,22,382,1356,2390,8000,16000,24486,50065,97479,140417,197514
    dd 345980,590827,803530,1183000,1483000,2759000,3753000,4649000,5937000
    ; 雇员数：21个2字节数值（单位：人）
    dw 3,7,9,13,28,38,130,220,476,778,1001,1442,2258,2793,4037,5635,8226
    dw 11542,14430,15257,17800
data ends

table segment
    ; 21行，每行16字节（对应题目中的“行内地址0~F”）
    db 21 dup ('year summ ne ?? ')  ; 占位，后续会被覆盖
table ends

codesg segment
start:
    ; 1. 初始化段寄存器
    mov ax, data
    mov ds, ax          ; ds指向data段
    mov ax, table
    mov es, ax          ; es指向table段

    ; 2. 初始化寄存器（循环21次，处理21年的数据）
    mov bx, 0           ; bx：table段中“行”的偏移（每行16字节，bx=0/16/32...）
    mov si, 0           ; si：data段中“年份”的偏移（每个年份占4字节）
    mov di, 0           ; di：table段中“行内”的偏移（0~F）
    mov cx, 21          ; cx：循环次数（21年）

s:
    ; ========== 步骤1：复制“年份”到table段 ==========
    ; data段年份地址：ds:[si]（4字节）
    ; table段目标地址：es:[bx+0]（行内地址0~3）
    mov ax, ds:[si]
    mov es:[bx+0], ax   ; 年份低2字节
    mov ax, ds:[si+2]
    mov es:[bx+2], ax   ; 年份高2字节

    ; ========== 步骤2：复制“收入”到table段 ==========
    ; data段收入地址：ds:[si+21*4]（21个年份占21*4=84字节，收入从84偏移开始）
    ; table段目标地址：es:[bx+5]（行内地址5~8）
    mov ax, ds:[si+84]
    mov es:[bx+5], ax   ; 收入低2字节
    mov ax, ds:[si+86]
    mov es:[bx+7], ax   ; 收入高2字节

    ; ========== 步骤3：复制“雇员数”到table段 ==========
    ; data段雇员数地址：ds:[si+21*4+21*4]（21个收入占21*4=84字节，雇员数从84+84=168偏移开始）
    ; table段目标地址：es:[bx+9]（行内地址9~A）
    mov ax, ds:[si+168]
    mov es:[bx+9], ax   ; 雇员数（2字节）

    ; ========== 步骤4：计算“人均收入”（收入 / 雇员数，取整） ==========
    ; 收入：ds:[si+84]（4字节，dx:ax）
    ; 雇员数：ds:[si+168]（2字节，cx）
    mov ax, ds:[si+84]
    mov dx, ds:[si+86]  ; dx:ax = 收入（4字节数值）
    mov cx, ds:[si+168] ; cx = 雇员数（2字节数值）
    div cx              ; ax = 商（人均收入），dx = 余数
    mov es:[bx+0Bh], ax ; 人均收入存入table段（行内地址B~C）

    ; ========== 步骤5：更新寄存器，准备下一次循环 ==========
    add si, 4           ; 年份偏移+4（下一个年份）
    add bx, 16          ; table段行偏移+16（下一行）
    loop s              ; cx--，若cx≠0则跳回s

    ; 3. 程序结束
    mov ax, 4c00h
    int 21h
codesg ends
end start